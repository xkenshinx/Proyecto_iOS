Como les comente, el Proyecto Final va a ser crear un "Spotify Streamer", el cual debe de permitir hacer lo siguiente:
Realizar una búsqueda por nombre de artistas y desplegar la lista junto con la foto de los artistas.
Cuando el usuario seleccione un artista, se debe de mostrar un listado con el top de canciones, junto con la imagen del album al que pertenece la canción.
Cuando el usuario seleccione una canción, la misma debe de reproducirse abriendo una ventana nueva para el reproductor, la cual debe tener los siguientes controles:
Play: reproducir canción
Pause: Pausar canción
Next: Ir a la siguiente canción de la lista
Previous: Ir a la canción anterior de la lista
Recuerden que para ello van a necesitar usar el API de Spotify (aquí la pueden encontrar) y para eso necesitan registrarse y registrar una aplicación para que obtengan un API Key.

